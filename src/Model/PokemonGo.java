public class PokemonGo {
    private String Bug;
    private Integer Dark;
    private Integer Dragon;
    private Integer Fighting;

    public String getBug() {
        return Bug;
    }

    public void setBug(String bug) {
        Bug = bug;
    }

    public Integer getDark() {
        return Dark;
    }

    public void setDark(Integer dark) {
        Dark = dark;
    }

    public Integer getDragon() {
        return Dragon;
    }

    public void setDragon(Integer dragon) {
        Dragon = dragon;
    }

    public Integer getFighting() {
        return Fighting;
    }

    public void setFighting(Integer fighting) {
        Fighting = fighting;
    }




}
